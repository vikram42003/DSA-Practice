from typing import List


class Solution:
    def isValidSudoku(self, board: List[List[str]]) -> bool:
        row_set = [set() for _ in range(9)]
        col_set = [set() for _ in range(9)]
        grid_set = [set() for _ in range(9)]

        for i in range(9):
            for j in range(9):
                cur = board[i][j]
                if cur == ".":
                    continue

                if (
                    cur in row_set[i]
                    or cur in col_set[j]
                    or cur in grid_set[((i // 3 ) * 3) + (j // 3)]
                ):
                    return False

                row_set[i].add(cur)
                col_set[j].add(cur)
                grid_set[((i // 3 ) * 3) + (j // 3)].add(cur)

        return True


